/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */
	SysTick_Add_Callback(&process_ms);
  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_USB_DEVICE_Init();
  MX_ADC1_Init();
  /* USER CODE BEGIN 2 */

	CarControlsReportData data = { 0 };
	data.reportId = (uint8_t) HID_REPORT_ID;
	data.steering = 1 << 15;

	// ADC
	HAL_ADC_Start_DMA(&hadc1, adc_values, ADC_CHANNEL_COUNT);
	double adc_convertion_factor = (double) (1<<8) / MAX_ADC_VALUE;

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
	uint8_t adc_byte_value[ADC_CHANNEL_COUNT] = {0};

	while (1) {
		if (!t_led) {
			t_led = 1000;
			HAL_GPIO_TogglePin(GPIOC, GPIO_PIN_13);
		}

		if (!t_usb) {
			t_usb = 100;

			// convert data
			for(uint8_t i=0; i<ADC_CHANNEL_COUNT; i++){
				if(adc_values[i] > MAX_ADC_VALUE){
					adc_byte_value[i] = (uint8_t) (2 << 8) - 1;
				}else{
					adc_byte_value[i] = (uint8_t) (adc_values[i] * adc_convertion_factor);
				}
			}

			data.accelerator = adc_byte_value[0];
			data.brake = adc_byte_value[1];
			data.clutch = adc_byte_value[2];

			USBD_HID_SendReport(&hUsbDeviceFS, (uint8_t*) &data, sizeof(data));
		}
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
	}
  /* USER CODE END 3 */
}

